# Create a string "s" with the value "I love to write python"
s = "I love to write python"

# Loop through "s" displaying each character
for char in s:
    print(char, end=' ')

# Print the 5th element of "s"
print("\n5th element of 's':", s[4])

# Print the last element of "s" using a negative index
print("Last element of 's':", s[-1])

# Print the length of "s"
print("Length of 's':", len(s))

# Try printing specific indices
print("s[0]:", s[0])
print("s[4]:", s[4])
print("s[10]:", s[10])
